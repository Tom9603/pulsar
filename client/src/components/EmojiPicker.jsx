const NAMES = Object.keys(GROUPS);

/** Sélecteur d'emoji réutilisable. onPick(emoji) est appelé au clic. */
export default function EmojiPicker({ onPick, onClose }) {
  const [tab, setTab] = useState(NAMES[0]);
  return (
    <div className="emoji-panel">
      <div className="emoji-tabs">
        {NAMES.map((n) => (
          <button key={n} className={tab === n ? 'active' : ''} title={n} onClick={() => setTab(n)}>
            {GROUPS[n][0]}
          </button>
        ))}
        {onClose && <button className="emoji-close" title="Fermer" onClick={onClose}>✕</button>}
      </div>
      <div className="emoji-grid">
        {GROUPS[tab].map((e, i) => (
          <button key={i} onClick={() => onPick(e)}>{e}</button>
        ))}
      </div>
    </div>
  );
}
